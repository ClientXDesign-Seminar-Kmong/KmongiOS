import Foundation
import UIKit

extension UIColor{
    @nonobjc class var sunYellow: UIColor {
        return UIColor(red: 246.0 / 255.0, green: 210.0 / 255.0, blue: 38.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var brownishGrey: UIColor {
        return UIColor(white: 101.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var veryLightPink: UIColor {
        return UIColor(white: 221.0 / 255.0, alpha: 1.0)
    }
}
